
#program to calculate area of triangle

#formula=1/2*base*hight

base_input = float(input("Enter base: "))
hight_input = float(input("Enter hight: "))
area_of_triangle = 1/2* (base_input * hight_input)

print("Area of the triangle is:" , area_of_triangle)
print(f"Area of the triangle is: {area_of_triangle:.2f}")